package com.propofol.www.user.portfolio.domain;

public class ExperienceSearch {
	private String exp_cd, title, contents, upload_img;

	public String getExp_cd() {
		return exp_cd;
	}

	public void setExp_cd(String exp_cd) {
		this.exp_cd = exp_cd;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContents() {
		return contents;
	}

	public void setContents(String contents) {
		this.contents = contents;
	}

	public String getUpload_img() {
		return upload_img;
	}

	public void setUpload_img(String upload_img) {
		this.upload_img = upload_img;
	}

	@Override
	public String toString() {
		return "ExperienceSearch [exp_cd=" + exp_cd + ", title=" + title + ", contents=" + contents + ", upload_img="
				+ upload_img + "]";
	}
	
} // class
